<script>
    var x = document.getElementById("demo");

    function getLocation() {
        document.getElementById("loginForm").addEventListener("submit", function (event) {
            event.preventDefault();
        });

        if (navigator.geolocation) {
            navigator.geolocation.getCurrentPosition(showPosition, showError);
        } else {
            x.innerHTML = "Geolocation is not supported by this browser.";
        }
    }

    function showPosition(position) {
        document.getElementById("loginForm").submit();
    }

    function showError(error) {
        switch (error.code) {
            case error.PERMISSION_DENIED:
                alert('Izin lokasi ditolak anda tidak bisa login sekarang, harap ubah settingan perizinan lokasi untuk login kedalam aplikasi');
                break;
            case error.POSITION_UNAVAILABLE:
                alert('Informasi lokasi tidak tersedia. anda tidak bisa login sekarang, harap ubah settingan perizinan lokasi untuk login kedalam aplikasi');
                break;
            case error.TIMEOUT:
                alert('Perizinan lokasi pengguna telah habih, harap ubah settingan perizinan lokasi untuk login kedalam aplikasi');
                break;
            default:
                alert('terjadi kesalahan saat mengizinkan lokasi, harap ubah settingan perizinan lokasi untuk login kedalam aplikasi');
                break;
        }
    }

</script>
